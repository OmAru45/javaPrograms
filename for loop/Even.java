package task1;

import java.util.Scanner;

// wap to print even and odd number
public class Even {
	public static void main(String[] args) {
		
		for(int i=1;i<=100;i++)
		{
			if(i % 2==0)
			{
				System.out.println(i+ "Number is Even ");
			}
			else
			{
				System.out.println(i+ "is a odd number");
			}
			
		}
	}

}
